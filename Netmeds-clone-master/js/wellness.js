function showWellProducts(p) {
  let products = p;
  
  let parent = document.querySelector(".deals-container");

  parent.innerHTML = null;

  products.forEach(function (el) {

    var div_1 = document.createElement("div");

    div_1.classList.add("card", "deals-card");

    var p_offer = document.createElement("p");

    p_offer.setAttribute("class", "deal-offer");

    p_offer.innerHTML = el.offer;

    var image = document.createElement("img");

    image.setAttribute("class", "card-img-top");

    image.src = el.image;

    var div_2 = document.createElement("div");

    div_2.setAttribute("class", "card-body");

    var heading = document.createElement("h15");

    heading.setAttribute("class", "deals-title-card");

    heading.innerHTML = el.name;

    var br1 = document.createElement("br");

    var italic = document.createElement("i");

    italic.setAttribute("class", "deals-text-card");

    italic.innerHTML = el.mkt;

    var br3 = document.createElement("br")

    var del = document.createElement("del");

    del.setAttribute("class", "deals-text-card");

    del.innerHTML = el.n_price;

    var d_price = document.createElement("p");

    d_price.setAttribute("class", "deals-price-card");

    d_price.innerHTML = el.d_price;

    var btn = document.createElement("button");

    btn.classList.add("btn", "deals-btn");

    btn.innerHTML = "Add to Cart";

    btn.addEventListener("click",function(){

      addToCart(el)

    });

    div_2.append(heading, br1, italic,br3, del, d_price, btn);

    div_1.append(p_offer, image, div_2);

    parent.append(div_1);
  });
}

